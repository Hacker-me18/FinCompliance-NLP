# -*- coding: utf-8 -*-
"""
LLM 合成数据 Prompt 模板（文档第 6 节风险标签体系 + 第 7.1 节 LLM 合成）

设计原则：
  - 每个风险类别对应一个专业财务 Prompt，引导 LLM 生成高仿真条款
  - Prompt 包含：角色设定（制造业财务专家）、标签定义、示例条款、生成要求
  - 通过 few-shot 示例控制生成风格与风险分布

6 类风险 Prompt 模板：
  0 normal_clause            生成常规履约条款
  1 payment_risk             生成付款周期风险条款（账期过长/可延期/高预付款）
  2 tax_invoice_risk         生成税务发票风险条款（开票延迟/发票类型模糊）
  3 delivery_acceptance_risk 生成交付验收风险条款（验收标准模糊/无时限）
  4 liability_penalty_risk   生成违约责任风险条款（责任不对等/高额违约金）
  5 financial_compliance_risk生成财务合规风险条款（个人账户/现金交易）
"""

# 各风险类别的 Prompt 模板字典（key: label_id, value: prompt 字符串）
PROMPT_TEMPLATES = {
    0: "你是制造业财务合同审核专家。请生成一条【正常履约条款】，描述基础合作约定，不涉及任何财务风险。示例：双方按照合同约定履行付款与交货义务。要求：生成 1 条，字数 20-50 字。",
    1: "你是制造业财务合同审核专家。请生成一条【付款周期风险】条款，表现为账期过长、可延期付款、超高预付款等。示例：乙方交付验收完成后180日内完成全款支付。要求：生成 1 条，字数 20-50 字，包含具体天数或延期条件。",
    2: "你是制造业财务合同审核专家。请生成一条【税务发票风险】条款，表现为开票时间延迟、无法开具增值税专票、发票类型模糊等。示例：甲方全额回款后，乙方方可开具增值税发票。要求：生成 1 条，字数 20-50 字。",
    3: "你是制造业财务合同审核专家。请生成一条【交付验收风险】条款，表现为验收标准模糊、无量化指标、无明确验收时限。示例：产品满足甲方使用需求后，甲方启动付款流程。要求：生成 1 条，字数 20-50 字。",
    4: "你是制造业财务合同审核专家。请生成一条【违约责任风险】条款，表现为甲乙双方责任不对等、乙方承担无限责任、高额违约金等。示例：乙方需承担项目所有直接、间接经济损失。要求：生成 1 条，字数 20-50 字。",
    5: "你是制造业财务合同审核专家。请生成一条【财务合规风险】条款，表现为现金大额交易、个人账户结算、关联非公允交易等。示例：甲方可通过私人账户向乙方结算货款。要求：生成 1 条，字数 20-50 字。",
}


def get_prompt_template(label_id: int) -> str:
    """
    获取指定风险类别的 Prompt 模板。

    Args:
        label_id: 风险标签 ID（0-5）

    Returns:
        str: 对应的 Prompt 模板字符串

    Raises:
        ValueError: label_id 不在 0-5 范围内
    """
    if label_id not in PROMPT_TEMPLATES:
        raise ValueError(f"无效的 label_id: {label_id}，有效范围 0-5")
    return PROMPT_TEMPLATES[label_id]
