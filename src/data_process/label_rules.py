# ========== 3. 单条打标函数 ==========
def label_clause(text: str) -> str:
    """
    给单条条款打标签。
    返回值：
      '0' / '1' / '2' / '3' / '4' / '5' / 'review' / 'discard'
    """
    if _is_discard(text):
        return "discard"
    if not _in_business(text):
        return "discard"

    hit_labels = []
    for lbl in [5, 1, 2, 3, 4]:   # 优先级：5 最高
        pos_hit = any(p in text for p in _RISK_POSITIVE.get(lbl, []))
        if not pos_hit:
            continue
        neg_hit = any(n in text for n in _RISK_NEGATIVE.get(lbl, []))
        if neg_hit:
            continue
        hit_labels.append(lbl)

    # 5 最高优先级，命中即直接返回 5（即使还命中别的）
    if 5 in hit_labels:
        return "5"
    # 多标签冲突
    if len(hit_labels) >= 2:
        return "review"
    if len(hit_labels) == 1:
        return str(hit_labels[0])
    return "0"
