# ========== 0. 文本预过滤（无关章节 / 占位符） ==========
_DISCARD_SECTIONS = [
    "技术参数", "图纸", "保密条款", "包装", "知识产权", "诉讼管辖",
    "投标函", "开标一览表", "货物说明一览表", "投标分项报价表", "技术要求",
    "规格型号", "评标办法", "资格审查", "维保方案", "中小企业声明函",
]
_DISCARD_PURE_PHRASES = ["", "无", "略", "见附件", "见下表", "无异议", "不参与"]


def _is_discard(text: str) -> bool:
    if not (18 <= len(text) <= 120):
        return True
    if text in _DISCARD_PURE_PHRASES:
        return True
    # 超过 30% 是占位符号（_、…、X） → 视为填空占位
    placeholder_chars = sum(1 for c in text if c in "_…×X")
    if placeholder_chars > len(text) * 0.3:
        return True
    for d in _DISCARD_SECTIONS:
        if d in text:
            return True
    return False


